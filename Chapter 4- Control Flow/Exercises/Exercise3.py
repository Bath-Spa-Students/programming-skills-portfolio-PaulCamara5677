#If block statement

alien_color = "green"
if alien_color == "green":
    print("You have scored 5 points for shooting the grunt alien.")
elif alien_color == "yellow":
    print("You have scored 10 points for shooting the bad alien.")
else:
    print("You have scored 15 points for shooting the boss alien.")

#Elif block statement

alien_color = "yellow"
if alien_color == "green" :
    print("You have scored 5 points for shooting the grunt alien.")
elif alien_color == "yellow" :
    print("You have scored 10 points for shooting the bad alien.")
else:
    print("You have scored 15 points for shooting the boss alien.")

