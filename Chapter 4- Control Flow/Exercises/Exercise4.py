#if-elif-else chain 

age = 1

if age < 2:
    print("You are a Baby.")
elif age <4:
    print("You are a young child.")
elif age <14:
    print("You are still a kid.")
elif age <20:
    print("You are now a teenager.")
elif age <29:
    print("You are now a young adult.")
elif age <75:
    print("You are now an Adult.")
else: 
    print("You are now an elderly person.")
