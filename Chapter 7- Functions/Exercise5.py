#Defining a city's country of origin or which country the city is located in.
def describe_city(city, country = ' UAE'):
#print will display a string in between the city and country value in order to make it clear where is where.
    print(city + "is located in" + country)
#Three examples as stated below
describe_city('Cabanatuan City', ' Philippines')
#Cabanatuan city is located in Philippines
describe_city('Dubai ')
#Dubai is located in UAE
describe_city('Ras Al-Khaimah ')
#Ras Al-Khaimah is located in UAE